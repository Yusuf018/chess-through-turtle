def rooks(rook):
    """
    Draws a rook chess piece.
    Receives a turtle object 'rook' to draw the piece.
    """
    rook.penup()
    rook.forward(20)
    rook.left(90)
    rook.forward(4)
    rook.right(90)
    rook.pendown()
    rook.pencolor("black")    
    rook.begin_fill()
    
    rook.forward(40)
    rook.left(90)
    rook.forward(15)
    rook.left(50)
    rook.forward(12)
    rook.right(50)
    rook.forward(20)
    rook.right(90)
    rook.forward(8)
    rook.left(90)
    rook.forward(24)
    rook.left(90)
    rook.forward(8)
    rook.left(90)
    rook.forward(8)
    rook.right(90)
    rook.forward(8)
    rook.right(90)
    rook.forward(8)
    rook.left(90)
    rook.forward(8)
    rook.left(90)
    rook.forward(8)
    rook.right(90)
    rook.forward(8)
    rook.right(90)
    rook.forward(8)
    rook.left(90)
    rook.forward(8)
    rook.left(90)
    rook.forward(24)
    rook.left(90)
    rook.forward(8)
    rook.right(90)
    rook.forward(20)
    rook.right(50)
    rook.forward(12)
    rook.left(50)
    rook.forward(15)
    rook.end_fill()